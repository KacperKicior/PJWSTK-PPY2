int1 = input("Podaj 1 liczbe: ")
int2 = input("Podaj 2 liczbe: ")
op = input("Podaj znak operacji: ")


match op:
    case '+':
        sum = int(int1) + int(int2)
        print("Wynik: " + str(sum))
    case '-':
        sum = int(int1) + int(int2)
        print("Wynik: " + str(sum))
    case '*':
        sum = int(int1) + int(int2)
        print("Wynik: " + str(sum))
    case '/':
        sum = int(int1) + int(int2)
        print("Wynik: " + str(sum))